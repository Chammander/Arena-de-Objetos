    private boolean validaInputs() {
        if (jtfNomeGuerreiro.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Preeencher o nome");
            jtfNomeGuerreiro.requestFocus();
            return false;
        }
        if (jtfCabeloGuerreiro.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Preeencher o cabelo");
            jtfCabeloGuerreiro.requestFocus();
            return false;
        }
        if (jtfOlhoGuerreiro.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Preeencher o olho");
            jtfOlhoGuerreiro.requestFocus();
            return false;
        }
        if (jtfPeleGuerreiro.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Preeencher a pele");
            jtfPeleGuerreiro.requestFocus();
            return false;
        }
        if(jtfArmaGuerreiro.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Preencher a arma");
            jtfArmaGuerreiro.requestFocus();
            return false;
        }
        if (!jrbFemininoGuerreiro.isSelected() && !jrbMasculinoGuerreiro.isSelected()) {
            JOptionPane.showMessageDialog(this, "Selecionar sexo!");
            return false;
        }//o sinal de ! está negando o retorno do select
        return true;
    }//validando dados
    
