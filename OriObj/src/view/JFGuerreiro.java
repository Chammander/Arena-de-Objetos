    public void addRowToTable(){
        //pega a modelagem da tabela na inteface grafica
        DefaultTableModel model = (DefaultTableModel) jtGuerreiro.getModel();
        model.getDataVector().removeAllElements();//remove linhas
        model.fireTableDataChanged();
        Object rowData[] = new Object[6];//vetor da tabela
        GuerreiroServicos guerreiroS = ServicosFactory.getGuerreiroServicos();
        //ler dados e add no vetor e tabela
        for (Guerreiro guerreiro : guerreiroS.listaGuerreiros()) {
            rowData[0] = guerreiro.getId();
            rowData[1] = guerreiro.getNome();
            rowData[2] = guerreiro.getPele();         
            rowData[3] = guerreiro.isSexo() == false ? "FEMININO" : "MASCULINO";//if ternary
            rowData[4] = guerreiro.getArmamento();
            rowData[5] = guerreiro.getPontoDeVida();
            model.addRow(rowData);
        }
    }//fim add to table
    
